# Arithmetic operator

print(10+7)
print(10-7)
print(7-10)
print(10*7)
print(11/4)

# floor division
print(11//4)
print(2**3)


# comparison operator
print("\n comparison\n")
print(10==10)
print(20>11)

# Assignment
print("\n Assignment\n")
a=10
a-=-20
print(a)





# logical operators

print("\n logical\n")

# and
print (10>5 and 10>20)
print (10>5 and 10<20)

# or
print (10>5 or 10>20)

# not
a=10
print(not a)


